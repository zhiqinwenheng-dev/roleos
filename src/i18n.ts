export const getKitTranslation = (kit: Kit, lang: Language) => {
  return {
    name: lang === 'zh' ? (kit as any).name_zh || kit.name : kit.name,
    description: lang === 'zh' ? (kit as any).description_zh || kit.description : kit.description,
    usage: lang === 'zh' ? (kit as any).usage_zh || kit.usage : kit.usage,
  };
};

export const getTeamTranslation = (team: Team, lang: Language) => {
  return {
    name: lang === 'zh' ? (team as any).name_zh || team.name : team.name,
    description: lang === 'zh' ? (team as any).description_zh || team.description : team.description,
    members: lang === 'zh' ? (team as any).members_zh || team.members : team.members,
  };
};
